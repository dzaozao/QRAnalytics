# ------------------ Titer 稳健提取（文本Token解析） ------------------
def titer_float(x):
    """ Titer专用的浮点数安全转换，遇到包含冒号的进样瓶号(Vial)直接跳过 """
    if ":" in str(x): 
        return None
    try:
        s = str(x).strip().replace(',', '')
        s = re.sub(r'[^\d.-]', '', s)
        return float(s) if s else None
    except:
        return None

def extract_titer_data(pdf_path, qc_theo_amount=None):
    """
    Titer 专用提取器：
    利用文本 Token 流状态机，有效规避由于长文本换行导致的表格错位问题。
    """
    print(f"开始解析 Titer PDF: {pdf_path}")
    sequence_name = "Unknown_Sequence"
    qc_data = []
    sample_data = []
    std_data = []
    eq_str = ""
    r2_val = None
    blank_area = None

    full_text = ""
    with pdfplumber.open(pdf_path) as pdf:
        for page in pdf.pages:
            text = page.extract_text() or ""
            full_text += text + "\n"

            # 1. 提取 Sequence Name
            if sequence_name == "Unknown_Sequence" and "Sample Set Name:" in text:
                match = re.search(r'Sample Set Name:\s*(\S+)', text)
                if match:
                    sequence_name = match.group(1).strip()

            # 2. 提取首个 Blank 的主峰峰面积
            if blank_area is None:
                if re.search(r'Sample\s*Name:\s*Blank', text, re.IGNORECASE):
                    lines = text.split('\n')
                    for line in lines:
                        if "Main Peak" in line:
                            # Main peak 的 Token 顺序通常为: [序号] Main Peak [RT] [%Area] [Area] [Amount]
                            parts = line.split("Main Peak")[-1].split()
                            nums = [safe_float(x) for x in parts if safe_float(x) is not None]
                            if len(nums) >= 3:
                                blank_area = nums[2]  # 第三个数字通常是 Area
                                break

    # 3. 提取 线性方程 和 R^2
    match_eq = re.search(r'Equation\s+(Y\s*=\s*[^;R]+)', full_text)
    if match_eq:
        eq_str = match_eq.group(1).strip()
    match_r2 = re.search(r'R\^2:\s*([\d.]+)', full_text)
    if match_r2:
        r2_val = safe_float(match_r2.group(1))

    # 4. 解析 Summary Table by QC (PDF表头可能为 Sample Set Table by QC)
        # 4. 解析 Summary Table by QC（改为按行正则解析，确保完整提取）
        # 4. 解析 Summary Table by QC（处理 Vial 列，确保提取全部 QC 行）
    qc_start = full_text.find("Sample Set Table by QC")
    qc_end = full_text.find("Sample Set Table by Sample")
    if qc_start != -1 and qc_end != -1:
        qc_block = full_text[qc_start:qc_end]
        lines = qc_block.split('\n')
        data_start = False
        for line in lines:
            line = line.strip()
            if not line:
                continue
            # 定位表头，开始数据行
            if "Sample Name" in line and "Vial" in line:
                data_start = True
                continue
            if not data_start:
                continue
            # 遇到统计行（% RSD）则停止
            if "% RSD" in line or "RSD" in line:
                break

            # 移除行首的数字序号（如 "1 ", "2 " 等）
            line_clean = re.sub(r'^\d+\s+', '', line)
            parts = line_clean.split()
            if len(parts) < 5:
                continue

            # 找到 Vial 列（包含 ':' 的 token）
            vial_idx = -1
            for i, part in enumerate(parts):
                if ':' in part:
                    vial_idx = i
                    break
            if vial_idx == -1:
                continue

            # 样品名为 Vial 之前的所有部分
            sample_name = ' '.join(parts[:vial_idx])
            # Vial 之后的 tokens 为数字列，提取前四个有效数字
            data_tokens = parts[vial_idx+1:]
            nums = []
            for t in data_tokens:
                val = safe_float(t)
                if val is not None:
                    nums.append(val)
                if len(nums) >= 4:
                    break

            # 仅当提取到四个数值且样品名包含 "QC" 时才添加
            if len(nums) >= 4 and "QC" in sample_name.upper():
                qc_data.append({
                    "Sample Name": sample_name,
                    "RT": nums[0],
                    "Area": nums[1],
                    "% Area": nums[2],
                    "Amount": nums[3]
                })


    # 5. 解析 Component Summary Table by sample (PDF表头可能为 Sample Set Table by Sample)
    sample_start = full_text.find("Table by Sample")
    sample_end = full_text.find("Calibration Plot")
    if sample_start != -1:
        block_end = sample_end if sample_end != -1 else len(full_text)
        sample_block = full_text[sample_start:block_end]
        tokens = sample_block.split()
        i = 0
        while i < len(tokens):
            # Empower的报告中普通样品通常为 Unknown
            if tokens[i].lower() in ["unknown", "standard", "control"] and i > 0:
                sample_name = tokens[i-1]
                sub_tokens = tokens[i+1:i+12]
                nums = [titer_float(x) for x in sub_tokens if titer_float(x) is not None]
                if len(nums) >= 4:
                    inj_vol = nums[0]
                    amount = nums[3]
                    # 过滤掉 Blank、QC 以及 STD
                    if (sample_name.upper() != "BLANK" and 
                        "QC" not in sample_name.upper() and 
                        "STD" not in sample_name.upper()):
                        sample_data.append({
                            "Sample Name": sample_name,
                            "Injection Volume": inj_vol,
                            "Amount": amount
                        })
                i += 1
            else:
                i += 1

    # 6. 解析 STD Calibration main peak
    std_start = full_text.find("Calibration main peak")
    if std_start != -1:
        std_block = full_text[std_start:std_start+2000]
        tokens = std_block.split()
        i = 0
        while i < len(tokens):
            if tokens[i].lower() == "level" and i+1 < len(tokens) and tokens[i+1].isdigit() and i > 0:
                sample_name = tokens[i-1]
                sub_tokens = tokens[i+2:i+12]
                nums = [titer_float(x) for x in sub_tokens if titer_float(x) is not None]
                if len(nums) >= 3:
                    std_data.append({
                        "Sample Name": sample_name,
                        "X Value": nums[0],          # 理论进样量
                        "Response": nums[1],         # 主峰峰面积
                        "Calc. Value": nums[2]       # 实测进样量
                    })
                i += 1
            else:
                i += 1

    return {
        "seq": sequence_name,
        "qc": qc_data,
        "sample": sample_data,
        "std": std_data,
        "eq": eq_str,
        "r2": r2_val,
        "blank_area": blank_area
    }

