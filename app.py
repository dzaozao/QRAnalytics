# ------------------ N-Glycan 稳健提取（文本解析） ------------------
def extract_nglycan_data(pdf_path):
    """
    使用文本行解析，准确定位 Component Summary Table N_Glycan 表格
    返回: (qc_list, sample_list, all_glycan_names)
        qc_list: [{'sample_name': str, 'gof_rt': float, 'gof_area': float}, ...]
        sample_list: [{'sample_name': str, 'glycans': {name: area}}, ...]
        all_glycan_names: 所有糖型名称列表（去重，others 放最后）
    """
    print("开始解析N-Glycan PDF（文本行解析模式）...")
    raw_data = []  # 存储每个样品的原始行数据，每个元素为 (sample_name, name, rt, area)
    current_sample = None
    collecting = False

    with pdfplumber.open(pdf_path) as pdf:
        for page_num, page in enumerate(pdf.pages, start=1):
            text = page.extract_text()
            if not text:
                continue
            lines = text.split('\n')
            for line in lines:
                # 1. 识别样品名（忽略Blank）
                if "Sample Name:" in line:
                    raw_name = line.split("Sample Name:")[-1].strip().split(' ')[0]
                    current_sample = None if "Blank" in raw_name else raw_name
                    collecting = False  # 新样品，重置表格收集标志
                    continue

                if not current_sample:
                    continue

                # 2. 定位表格标题
                if "Component Summary Table N_Glycan" in line:
                    collecting = True
                    continue

                # 3. 收集数据行
                if collecting:
                    # 遇到下一个样品信息，停止收集
                    if "Sample Information" in line:
                        collecting = False
                        continue

                    # 解析行：糖型名称可能包含空格，但 RT, Area, %Area 为最后三个字段
                    parts = line.split()
                    if len(parts) >= 4:
                        # 最后三个字段：RT, Area, %Area
                        rt_val = safe_float(parts[-3])
                        area_val = safe_float(parts[-2])   # Area 列，暂时不用
                        pct_val = safe_float(parts[-1])
                        if rt_val is not None and pct_val is not None:
                            # 糖型名称 = 前几个字段合并，并去除前缀数字
                            name_raw = " ".join(parts[:-3])
                            # 清洗：去掉开头的数字序号，如 "1 Others" -> "Others"
                            clean_name = re.sub(r'^\d+\s+', '', name_raw).strip()
                            raw_data.append((current_sample, clean_name, rt_val, pct_val))
                            print(f"  提取: {current_sample} -> {clean_name}, RT={rt_val}, %Area={pct_val}")

    # 按样品名和糖型名称聚合（因为可能出现同名项，如多个Others需要累加）
    # 但根据PDF，每个样品每个糖型只有一行，无需累加，但为了健壮性保留聚合逻辑
    # 构建字典: sample_name -> {glycan_name: area_pct}
    sample_glycans = {}
    qc_data = []
    all_names = set()

    for sample_name, name, rt, area in raw_data:
        if "QC" in sample_name.upper():
            # QC 样品只记录 G0F
            if name.upper() == "G0F":
                qc_data.append({
                    'sample_name': sample_name,
                    'gof_rt': rt,
                    'gof_area': area
                })
        else:
            # 普通样品
            if sample_name not in sample_glycans:
                sample_glycans[sample_name] = {}
            sample_glycans[sample_name][name] = area
            all_names.add(name)

    # 将普通样品转换为列表
    sample_data = [{'sample_name': name, 'glycans': gly_dict} for name, gly_dict in sample_glycans.items()]

    # 整理糖型列表：排序，others放最后
    all_names = sorted(all_names)
    if 'others' in all_names:
        all_names.remove('others')
        all_names.append('others')
    elif 'Others' in all_names:
        all_names.remove('Others')
        all_names.append('Others')

    print(f"解析完成: QC样品数={len(qc_data)}, 普通样品数={len(sample_data)}, 糖型列表={all_names}")
    return qc_data, sample_data, all_names

