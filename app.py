def extract_sec_bf611_data(pdf_path, project=None):
    """
    针对 BF611 / BF422 SEC 项目的提取函数
    返回: {'qc': [...], 'samples': [...], 'seq': '序列名'}
    """
    print(f"开始解析 SEC PDF (BF611/BF422): {os.path.basename(pdf_path)}")
    qc_data = []
    sample_results = []
    sequence_name = "Unknown_Sequence"

    with pdfplumber.open(pdf_path) as pdf:
        # ---------- 全局提取 Sample Set Name ----------
        for page in pdf.pages:
            full_text = page.extract_text() or ""
            if "Sample Set Name:" in full_text:
                match = re.search(r'Sample Set Name:\s*([\w-]+)', full_text)
                if match:
                    sequence_name = match.group(1).strip()
                    break

        # ---------- 逐页处理 ----------
        for page in pdf.pages:
            text = page.extract_text() or ""
            tables = page.extract_tables()

            # 1. 获取当前页面的 Sample Name（用于 QC 判断）
            current_sample = None
            if "Sample Name:" in text:
                s_match = re.search(r'Sample Name:\s*(\S+)', text)
                if s_match:
                    current_sample = s_match.group(1).strip()

            # 2. 处理 QC 样品 (系统适用性) - 保持不变
            if current_sample and "QC" in current_sample:
                qc_item = {'Sample Name': current_sample}
                table_found = False
                for table in tables:
                    if not table or len(table) < 2:
                        continue
                    header = [str(c).strip() if c else "" for c in table[0]]
                    header_str = " ".join(header).lower()
                    if "component summary table by all peak" in text.lower() and "end p/v" in header_str:
                        table_found = True
                        idx_name = next((i for i, h in enumerate(header) if h and "name" in h.lower()), 0)
                        idx_rt = next((i for i, h in enumerate(header) if h and "rt" in h.lower()), 1)
                        idx_area = next((i for i, h in enumerate(header) if h and "area" in h.lower() and "%" not in h), 2)
                        idx_pct = next((i for i, h in enumerate(header) if h and "% area" in h.lower()), 3)
                        idx_usp = next((i for i, h in enumerate(header) if h and "usp plate count" in h.lower()), 5)
                        idx_start_pv = next((i for i, h in enumerate(header) if h and "start p/v" in h.lower()), -1)
                        idx_end_pv = next((i for i, h in enumerate(header) if h and "end p/v" in h.lower()), -1)

                        if idx_end_pv != -1:
                            for row in table[1:]:
                                if len(row) > idx_end_pv:
                                    val = safe_float(row[idx_end_pv])
                                    if val is not None:
                                        qc_item['End_pv_first'] = val
                                        break

                        for row in table[1:]:
                            if len(row) <= max(idx_name, idx_rt, idx_area, idx_pct, idx_usp):
                                continue
                            name_cell = str(row[idx_name]) if idx_name < len(row) else ""
                            if "Main peak" in name_cell or "D-Protein" in name_cell:
                                qc_item['RT'] = safe_float(row[idx_rt]) if idx_rt < len(row) else None
                                qc_item['Area'] = safe_float(row[idx_area]) if idx_area < len(row) else None
                                qc_item['% Area'] = safe_float(row[idx_pct]) if idx_pct < len(row) else None
                                qc_item['USP'] = safe_float(row[idx_usp]) if idx_usp < len(row) else None
                            elif "M-protien" in name_cell or "M-protein" in name_cell:
                                if idx_start_pv != -1 and len(row) > idx_start_pv:
                                    qc_item['M_protein_start_pv'] = safe_float(row[idx_start_pv])

                if not table_found or 'RT' not in qc_item:
                    qc_fallback = extract_qc_from_text_fallback(text, current_sample)
                    if qc_fallback:
                        qc_item.update(qc_fallback)

                if 'RT' in qc_item:
                    qc_data.append(qc_item)

            # 3. 处理普通样品表格 (Component Summary Table by sample)
            # 使用与通用SEC一致的表格解析策略
            if "Component Summary Table by sample" in text:
                sample_results.extend(parse_sec_bf611_sample_from_page(page, text))

    return {
        "qc": qc_data,
        "samples": sample_results,
        "seq": sequence_name
    }


def parse_sec_bf611_sample_from_page(page, text):
    """
    解析 Component Summary Table by sample 表格，提取普通样品数据（排除QC和Blank）
    """
    sample_rows = []
    tables = page.extract_tables()
    for table in tables:
        if not table or len(table) < 2:
            continue
        header = [str(cell).strip() if cell else "" for cell in table[0]]
        header_str = " ".join(header).lower()

        # 严格识别 Component Summary Table by sample 表格（避免误匹配其他表格）
        if "component summary table by sample" not in text.lower() and "component summary table by sample" not in header_str:
            continue

        # 动态定位列索引
        idx_sample = next((i for i, h in enumerate(header) if "sample name" in h.lower()), 0)
        idx_hmw = next((i for i, h in enumerate(header) if h.upper() == "HMWS" or "hmw" in h.lower()), -1)
        idx_lmw = next((i for i, h in enumerate(header) if h.upper() == "LMWS" or "lmw" in h.lower()), -1)
        idx_main = next((i for i, h in enumerate(header) if "main peak" in h.lower()), -1)
        idx_mprot = next((i for i, h in enumerate(header) if "m-protien" in h.lower() or "m-protein" in h.lower()), -1)

        for row in table[1:]:
            if not row or len(row) <= max(idx_sample, idx_hmw, idx_lmw, idx_main, idx_mprot):
                continue
            sample_name = str(row[idx_sample]).strip() if idx_sample < len(row) else ""
            # ---------- 增强过滤：排除 Blank、纯数字、含RSD、含%以及任何含QC的行 ----------
            if (not sample_name or 
                sample_name.upper() == 'BLANK' or 
                sample_name.isdigit() or
                'RSD' in sample_name.upper() or
                '%' in sample_name or
                'QC' in sample_name.upper()):   # 关键：过滤掉QC样品
                continue

            # 清洗样品名（去除可能的前导序号）
            sample_name = re.sub(r'^\d+\s*', '', sample_name)

            hmw = safe_float(row[idx_hmw]) if idx_hmw != -1 and idx_hmw < len(row) else None
            lmw = safe_float(row[idx_lmw]) if idx_lmw != -1 and idx_lmw < len(row) else None
            main = safe_float(row[idx_main]) if idx_main != -1 and idx_main < len(row) else None
            mprot = safe_float(row[idx_mprot]) if idx_mprot != -1 and idx_mprot < len(row) else None

            # 计算其他片段
            other = None
            if lmw is not None and mprot is not None:
                other = lmw - mprot

            sample_rows.append({
                'Sample Name': sample_name,
                'HMWS': hmw,
                'LMWS': lmw,
                'Main': main,
                'M-protein': mprot,
                'Other': other
            })
    return sample_rows


# QC文本兜底函数（保持不变）
def extract_qc_from_text_fallback(text, sample_name):
    """QC 数据文本解析兜底函数"""
    start_marker = "Component Summary Table by all peak"
    start_idx = text.find(start_marker)
    if start_idx == -1:
        return None
    search_text = text[start_idx:]
    lines = search_text.split('\n')
    result = {}
    for line in lines:
        if "Main peak" in line or "D-Protein" in line:
            nums = [safe_float(p) for p in line.split() if safe_float(p) is not None]
            if len(nums) >= 5:
                result['RT'] = nums[0]
                result['Area'] = nums[1]
                result['% Area'] = nums[2]
                result['USP'] = nums[4]
                if len(nums) >= 8:
                    result['End_pv_first'] = nums[7]
        elif "M-protien" in line or "M-protein" in line:
            nums = [safe_float(p) for p in line.split() if safe_float(p) is not None]
            if len(nums) >= 7:
                result['M_protein_start_pv'] = nums[6]
    return result if result else None
