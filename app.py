        ws_rep[f"F{row_idx}"] = pharmacopoeia_round(ng_v, 1) if ng_v is not None else "N/D"

        if lc_v is not None and hc_v is not None:
            ws_rep[f"G{row_idx}"] = pharmacopoeia_round(lc_v + hc_v, 1)
        else:
            ws_rep[f"G{row_idx}"] = "N/D"

