_COMPANY_DEPT = "QR部门"
_AUTHOR_NAME = "邓永杰"
_WATERMARK_KEY = "InternalToolOrigin"
_WATERMARK_VAL = f"Generated by {_COMPANY_NAME} {_COMPANY_DEPT} AutoReport - Author: {_AUTHOR_NAME}"

def inject_excel_watermark(wb_or_path):
    """
    向 Excel 文件注入隐蔽溯源水印。
    兼容 openpyxl 2.5+ 所有版本。
    """
    try:
        if isinstance(wb_or_path, Workbook):
            wb = wb_or_path
            need_save = True
        else:
            wb = openpyxl.load_workbook(wb_or_path)
            need_save = True

        # ----- 标准属性（右键→属性→详细信息 可见）-----
        wb.properties.creator = _AUTHOR_NAME
        wb.properties.lastModifiedBy = _AUTHOR_NAME
        wb.properties.company = _COMPANY_NAME
        wb.properties.category = f"{_COMPANY_NAME}-{_COMPANY_DEPT}"
        wb.properties.description = "本文件含数字水印，仅限内部使用"

        # ----- 自定义隐藏属性 -----
        # 创建属性对象（兼容不同版本）
        prop = None
        try:
            from openpyxl.packaging.custom import StringProperty
            prop = StringProperty(name=_WATERMARK_KEY, value=_WATERMARK_VAL)
        except ImportError:
            try:
                from openpyxl.packaging.custom import StringItem
                prop = StringItem(name=_WATERMARK_KEY, value=_WATERMARK_VAL)
            except ImportError:
                # 终极兜底：使用简单对象
                class SimpleProp:
                    def __init__(self, name, value):
                        self.name = name
                        self.value = value
                prop = SimpleProp(_WATERMARK_KEY, _WATERMARK_VAL)

        # 移除已存在的同名属性（防止重复）
        if hasattr(wb, 'custom_doc_props'):
            props = wb.custom_doc_props
            # 遍历寻找并删除同名属性
            to_remove = []
            for i, p in enumerate(props):
                if hasattr(p, 'name') and p.name == _WATERMARK_KEY:
                    to_remove.append(i)
            # 从后往前删除，避免索引错乱
            for idx in reversed(to_remove):
                del props[idx]
            # 添加新属性
            props.append(prop)

        # 保存文件（如果传入的是路径）
        if need_save and isinstance(wb_or_path, str):
            wb.save(wb_or_path)
            wb.close()
        elif not isinstance(wb_or_path, Workbook):
            wb.close()

        return True
    except Exception as e:
        print(f"[水印提示] 写入失败（不影响报告）: {e}")
        return False




RRT_TOLERANCE = 0.008

def safe_float(x):
    if x is None:
        return None
    if isinstance(x, (int, float)):
        return float(x)
    try:
        s = str(x).strip().replace(',', '')
        s = re.sub(r'[^\d.-]', '', s)
        return float(s) if s else None
    except:
        return None


def pharmacopoeia_round(x, decimals=1):
    """
    严格执行《中国药典》“四舍六入五成双”修约规则。
    """
    if x is None or x == '':
        return None
    try:
        ctx = decimal.getcontext()
        ctx.rounding = decimal.ROUND_HALF_EVEN
        d = decimal.Decimal(str(x))
        fmt = '0.' + '0' * decimals if decimals > 0 else '0'
        return float(d.quantize(decimal.Decimal(fmt), rounding=decimal.ROUND_HALF_EVEN))
    except:
        return None

def ceil_up_rsd(val, places=1):
    """
    RSD 修约规则：只进不舍（进一法）。
    """
    if val is None: return None
    try:
        factor = 10 ** places
        return math.ceil(val * factor) / factor
    except: return None

def calculate_rsd(values):
    """计算百分比 RSD"""
    if not values or len(values) < 2: return None
    df = pd.Series(values)
    mean = df.mean()
    if mean == 0: return 0
    return (df.std() / mean) * 100        

def safe_str(x):
    return str(x).strip() if x else ''

# ------------------ IEC 提取 ------------------
def extract_cex_data(pdf_path, project=None):
    """
    CEX 专用提取器（文本解析版）：
    1. 提取 Sample Set Name（序列名）
    2. 从文本中解析 Summary Table by QC 表格数据
    3. 解析 %Area Summarized by Name 表格数据（保留原表格提取方式，因该表结构稳定）
    """
    print(f"开始处理 CEX PDF: {pdf_path}")
    sequence_name = "Unknown_Sequence"
    qc_data = []          # 存放 QC 表数据
    summary_data = []     # 存放 %Area 汇总表数据

    with pdfplumber.open(pdf_path) as pdf:
        for page in pdf.pages:
            text = page.extract_text() or ""
            # ---------- 提取 Sample Set Name ----------
            if "Sample Set Name:" in text:
                match = re.search(r'Sample Set Name:\s*(.*)', text)
                if match:
                    seq_raw = match.group(1).strip()
                    seq_raw = seq_raw.split('  ')[0].strip() 
                    sequence_name = seq_raw

            # ---------- 解析 Summary Table by QC（文本方式）----------
            if "Summary Table by QC" in text:
                qc_data.extend(parse_qc_from_text(text))

            # ---------- 解析 %Area Summarized by Name（仍用表格，结构稳定）----------
            tables = page.extract_tables()
            for table in tables:
                if not table or len(table) < 2:
                    continue
                header = [str(cell).strip() if cell else "" for cell in table[0]]
                header_str = " ".join(header).lower()
                if "% area summarized by name" in text.lower() and "basic peaks" in header_str:
                    rows = parse_summary_table(table)
                    summary_data.extend(rows)

    # 去重（若同一QC样品出现多次，保留全部）
    return {"qc": qc_data, "summary": summary_data, "seq": sequence_name}


def parse_qc_from_text(text):
    qc_rows = []
    lines = text.split('\n')
    start_idx = -1
    for i, line in enumerate(lines):
        if "Summary Table by QC" in line:
            start_idx = i + 1
            break
    if start_idx == -1:
        return qc_rows

    header_found = False
    for i in range(start_idx, len(lines)):
        line = lines[i].strip()
        if not line:
            continue
        if not header_found and ("SampleName" in line or "Retention Time" in line):
            header_found = True
            continue
        if not header_found:
            continue
        if "Main peak" not in line:
            if "Blank" in line or "% Area Summarized" in line:
                break
            continue

        # 尝试正则提取
        match = re.search(r'^(.+?)\s+Main peak\s+([\d.]+)\s+(\d+)\s+([\d.]+)\s+(\d+)', line)
        if match:
            sample_name = match.group(1).strip()
            rt = safe_float(match.group(2))
            area = safe_float(match.group(3))
            pct = safe_float(match.group(4))
            usp = safe_float(match.group(5))
        else:
            # 备用方案：按空格分割
            parts = line.split()
            try:
                mp_idx = parts.index("Main")
                if parts[mp_idx+1] == "peak":
                    sample_name = " ".join(parts[:mp_idx])
                    rt = safe_float(parts[mp_idx+2])
                    area = safe_float(parts[mp_idx+3])
                    pct = safe_float(parts[mp_idx+4])
                    usp = safe_float(parts[mp_idx+5])
            except (ValueError, IndexError):
                continue

        # 清洗样品名：去除前导数字序号（如 "1QC-..." -> "QC-..."）
        sample_name = re.sub(r'^\d+\s*', '', sample_name.strip())
        if not sample_name:
            continue

        qc_rows.append({
            'Sample Name': sample_name,
            'RT': rt,
            'Area': area,
            '% Area': pct,
            'USP': usp
        })
    return qc_rows

def parse_summary_table(table):
    """解析 %Area Summarized by Name 表格，返回字典列表"""
    rows_data = []
    if not table or len(table) < 2:
        return rows_data

    header = [str(cell).strip() if cell else "" for cell in table[0]]
    # 定位列索引
    idx_sample = next((i for i, h in enumerate(header) if "samplename" in h.lower()), 0)
    # Basic peaks, Acidic peaks, Main peak 可能在列名中直接给出
    idx_basic = next((i for i, h in enumerate(header) if "basic peaks" in h.lower()), -3)
    idx_acidic = next((i for i, h in enumerate(header) if "acidic peaks" in h.lower()), -2)
    idx_main = next((i for i, h in enumerate(header) if "main peak" in h.lower()), -1)

    for row in table[1:]:
        if not row:
            continue
        max_idx = max(idx_sample, idx_basic, idx_acidic, idx_main)
        if len(row) <= max_idx:
            continue

        sample_name = str(row[idx_sample]).strip()
        # 过滤空白、纯数字、BLANK
        if not sample_name or sample_name.isdigit() or sample_name.upper() == 'BLANK':
            continue

        rows_data.append({
            'Sample Name': sample_name,
            'Basic': safe_float(row[idx_basic]) if idx_basic < len(row) else None,
            'Acidic': safe_float(row[idx_acidic]) if idx_acidic < len(row) else None,
            'Main': safe_float(row[idx_main]) if idx_main < len(row) else None
        })
    return rows_data
# ------------------ RP 提取 ------------------
def extract_rp_data(pdf_path, rrt_params):
    print("开始处理PDF:", pdf_path, flush=True)
    qc_ref = rrt_params['qc_ref']
    sample_refs = rrt_params['sample_refs']
    all_samples = []
    with pdfplumber.open(pdf_path) as pdf:
        for page_num, page in enumerate(pdf.pages, start=1):
            text = page.extract_text()
            if not text:
                continue
            text_upper = re.sub(r'\s+', '', text.upper())
            if "SAMPLE" not in text_upper or "INFORMATION" not in text_upper:
                continue
            sample_match = re.search(r'Sample\s*Name:\s*(\S+)', text)
            if not sample_match:
                continue
            sample_name = sample_match.group(1)
            inj_vol_match = re.search(r'Injection\s*Volume:\s*([\d.]+)\s*ul', text)
            inj_vol = safe_float(inj_vol_match.group(1)) if inj_vol_match else None
            tables = page.extract_tables()
            peak_table = None
            for table in tables:
                if table and len(table) > 1 and any('Name' in str(cell) for cell in table[0]):
                    peak_table = table
                    break
            if not peak_table:
                continue
            header = peak_table[0]
            name_col = rt_col = area_pct_col = area_col = amount_col = usp_col = res_col = None
            for i, cell in enumerate(header):
                cell_str = safe_str(cell)
                if 'Name' in cell_str:
                    name_col = i
                elif 'RT' in cell_str:
                    rt_col = i
                elif '% Area' in cell_str:
                    area_pct_col = i
                elif 'Area' in cell_str and '%' not in cell_str:
                    area_col = i
                elif 'Amount' in cell_str:
                    amount_col = i
                elif 'USP Plate Count' in cell_str:
                    usp_col = i
                elif 'Resolution' in cell_str:
                    res_col = i
            required_cols = [name_col, rt_col, area_pct_col, area_col, amount_col, usp_col, res_col]
            max_col = max([c for c in required_cols if c is not None])
            value_cols = [col for col in [rt_col, area_pct_col, area_col, amount_col, usp_col, res_col] if col is not None]
            value_cols.sort()
            peaks = []
            for row in peak_table[1:]:
                if len(row) <= max_col:
                    continue
                name = safe_str(row[name_col]) if name_col is not None and len(row) > name_col else ''
                if not name:
                    continue
                rt = safe_float(row[rt_col]) if rt_col is not None and len(row) > rt_col else None
                area_pct = safe_float(row[area_pct_col]) if area_pct_col is not None and len(row) > area_pct_col else None
                area = safe_float(row[area_col]) if area_col is not None and len(row) > area_col else None
                amount = safe_float(row[amount_col]) if amount_col is not None and len(row) > amount_col else None
                usp = safe_float(row[usp_col]) if usp_col is not None and len(row) > usp_col else None
                res = safe_float(row[res_col]) if res_col is not None and len(row) > res_col else None
                peaks.append({
                    'name': name,
                    'rt': rt,
                    'area_pct': area_pct,
                    'area': area,
                    'amount': amount,
                    'usp': usp,
                    'resolution': res
                })
 
            main_peak = next((p for p in peaks if 'main' in p['name'].lower()), None)
            if main_peak:
                main_peak_line = None
                lines = text.split('\n')
                for line in lines:
                    if 'Main Peak' in line:
                        main_peak_line = line
                        break
                
                if main_peak_line:
                    # 分割 'Main Peak' 后的文本，避免行首可能存在的峰序号（如 '4'）干扰提取
                    post_text = main_peak_line.split('Main Peak')[-1]
                    
                    # 使用正则单独剥离出完整的数字区间（保留有效空格隔离），避免粘连
                    nums = [safe_float(x) for x in re.findall(r'\d+\.\d+|\d+', post_text) if safe_float(x) is not None]
                    
                    # Empower 默认主峰字段顺序：RT, %Area, Area, Amount, USP Plate Count, [Resolution], [Dilution]
                    # 只要提取出至少前 5 个核心数据，即可强制修复
                    if len(nums) >= 5:
                        main_peak['rt'] = nums[0]
                        main_peak['area_pct'] = nums[1]
                        main_peak['area'] = nums[2]
                        # 绕过表格提取缺陷，强制赋予以文本解析出的一一对应的值
                        main_peak['amount'] = nums[3]
                        main_peak['usp'] = nums[4]
                        
                        # 判断是否包含 Resolution（主峰如果非出峰首位且存在7个数据，第6个为Res，第7个为Dilution）
                        if len(nums) >= 7:
                            main_peak['resolution'] = nums[5]
            all_samples.append({
                'sample_name': sample_name,
                'inj_vol': inj_vol,
                'peaks': peaks
            })
    qc_list = [s for s in all_samples if 'QC' in s['sample_name'].upper()]
    std_list = [s for s in all_samples if 'STD' in s['sample_name'].upper()]
    sample_list = [s for s in all_samples if 'QC' not in s['sample_name'].upper() and 'STD' not in s['sample_name'].upper() and 'BLANK' not in s['sample_name'].upper()]
    qc_rows = []
    for s in qc_list:
        main_peak = next((p for p in s['peaks'] if 'main' in p['name'].lower()), None)
        if not main_peak or not main_peak['rt']:
            continue
        main_rt = main_peak['rt']
        qc_match_res = None
        for p in s['peaks']:
            if p['rt'] and main_rt:
                rrt = p['rt'] / main_rt
                if abs(rrt - qc_ref) <= RRT_TOLERANCE and p['resolution'] is not None:
                    qc_match_res = p['resolution']
                    break
        qc_rows.append({
            'Sample Name': s['sample_name'],
            'RT': main_peak['rt'],
            '% Area': main_peak['area_pct'],
            'Area': main_peak['area'],
            'USP Plate Count': main_peak['usp'],
            'Resolution': main_peak['resolution'],
            'Amount': main_peak['amount'],
            'RRT_Resolution': qc_match_res
        })
    qc_df = pd.DataFrame(qc_rows)
    std_rows = []
    for s in std_list:
        main_peak = next((p for p in s['peaks'] if 'main' in p['name'].lower()), None)
        if main_peak:
            std_rows.append({'Sample Name': s['sample_name'], 'Area': main_peak['area'], 'Amount': main_peak['amount']})
    std_df = pd.DataFrame(std_rows)
    sample_rows = []
    for s in sample_list:
        main_peak = next((p for p in s['peaks'] if 'main' in p['name'].lower()), None)
        if not main_peak or not main_peak['rt']:
            continue
        main_rt = main_peak['rt']
        rrt_matches = {ref: None for ref in sample_refs}
        for p in s['peaks']:
            if p['rt'] and main_rt:
                rrt = p['rt'] / main_rt
                for ref in sample_refs:
                    if abs(rrt - ref) <= RRT_TOLERANCE and rrt_matches[ref] is None:
                        rrt_matches[ref] = p['area_pct']
                        break
        others_sum = 0
        for p in s['peaks']:
            if 'main' in p['name'].lower():
                continue
            if p['rt'] and main_rt:
                rrt = p['rt'] / main_rt
                if not any(abs(rrt - ref) <= RRT_TOLERANCE for ref in sample_refs) and p['area_pct']:
                    others_sum += p['area_pct']
            elif p['area_pct']:
                others_sum += p['area_pct']
        conc = (main_peak['amount'] / s['inj_vol'] * 1000) if s['inj_vol'] and main_peak['amount'] else None
        sample_rows.append({
            'Sample Name': s['sample_name'],
            'main peak _Area': main_peak['area'],
            'RRT1': rrt_matches[sample_refs[0]] if len(sample_refs) > 0 else None,
            'RRT2': rrt_matches[sample_refs[1]] if len(sample_refs) > 1 else None,
            'RRT3': rrt_matches[sample_refs[2]] if len(sample_refs) > 2 else None,
            'Main peak % Area': main_peak['area_pct'],
            'others': others_sum,
            'amount': main_peak['amount'],
            'injection volume': s['inj_vol'],
            'concentration': conc
        })
    sample_df = pd.DataFrame(sample_rows)
    return qc_df, std_df, sample_df
